#define LOG_CYAN    "\033[0;36m"
#define LOG_GRAY    "\033[0;37m"
#define LOG_WHITE   "\033[0;38m"
#define LOG_RESET   "\033[0m"

#define BOLD_BLACK   "\033[1;30m"
#define BOLD_RED     "\033[1;31m"
#define BOLD_GREEN   "\033[1;32m"
